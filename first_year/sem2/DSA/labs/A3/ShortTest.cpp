#include <assert.h>
#include <vector>
#include<iostream>

void testAll() {
	MultiMap m;
	m.add(1, 100);
	m.add(2, 200);
	m.add(3, 300);
	m.add(1, 500);
	m.add(2, 600);
	m.add(4, 800);

	assert(m.size() == 6);

	assert(m.remove(5, 600) == false);
	assert(m.remove(1, 500) == true);

	assert(m.size() == 5);

    vector<TValue> v;
	v=m.search(6);
	assert(v.size()==0);

	v=m.search(1);
	assert(v.size()==1);

	assert(m.isEmpty() == false);

	MultiMapIterator im = m.iterator();
	assert(im.valid() == true);
	while (im.valid()) {
		im.getCurrent();
		im.next();
	}
	assert(im.valid() == false);
	im.first();
	assert(im.valid() == true);
}

void testMostFrequent()
{
	cout << "Test most frequent\n";

	MultiMap m;

	assert(m.mostFrequent() == NULL_TVALUE);

	m.add(1, 100);

	assert(m.mostFrequent() == 100);

	m.add(2, 200);
	m.add(3, 300);
	m.add(1, 500);
	m.add(2, 600);
	m.add(4, 800);

	m.add(4, 100);
	m.add(4, 200);
	m.add(3, 300);
	m.add(1, 500);
	m.add(2, 600);
	m.add(4, 800);

	m.add(2, 100); // this value appears the most times (3)

	assert(m.mostFrequent() == 100);

	m.add(3, 200);
	m.add(4, 200);

	assert(m.mostFrequent() == 200); // now the most frequent value has changed (this appears 4 times)
}